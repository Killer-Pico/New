function ToggleAnswer(answer1) {
    let answer = document.getElementById(answer1);
    if (answer1) {
       
        if (answer.style.display === "none" || answer.style.display === "") {
            answer.style.display = "block";
        } else {
            answer.style.display = "none";
}
    }
}
